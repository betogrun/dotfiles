        vim.cmd("nmap ySs      <Plug>YSsurround")
        vim.cmd("nmap ySS      <Plug>YSsurround")
        vim.cmd("xmap gs       <Plug>VSurround")
        vim.cmd("xmap gS       <Plug>VgSurround")
